# ---- Card Container ----
st.markdown("<div class='card'>", unsafe_allow_html=True)

# Text Input
text_input = st.text_area(
    "Enter a News Headline",
    placeholder="Example: 'Stock markets rise after new economic reforms'",
    height=120
)

# Predict button
btn = st.button("Analyze Sentiment")

# ---- Prediction Logic ----
if btn:
    if text_input.strip() == "":
        st.warning("Please enter a news headline.")
    else:
        vec = vectorizer.transform([text_input])
        pred = model.predict(vec)[0]

        # UI result box
        if pred == "positive":
            color_class = "positive"
            label = "Positive üî•"
        elif pred == "negative":
            color_class = "negative"
            label = "Negative ‚ö†Ô∏è"
        else:
            color_class = "neutral"
            label = "Neutral üü°"

        st.markdown(
            f"<div class='result-box {color_class}'>{label}</div>",
            unsafe_allow_html=True
        )

st.markdown("</div>", unsafe_allow_html=True)